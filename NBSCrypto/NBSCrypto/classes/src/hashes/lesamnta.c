#define ROUND256(a,b,c,d)					\
    tmp.msg[0] = midmsg[a]	^ midkey[b];			\
    tmp.msg[1] = midmsg[a + 1]	^ midkey[b + 1];		\
    tmp.key[0] = midkey[a]	^ (unsigned int)CNS[d][0];	\
    tmp.key[1] = midkey[a + 1]	^ (unsigned int)CNS[d][1];	\
    F256_1(tmp.key,tmp.t);					\
    midkey[c]     ^= tmp.key[0];				\
    midkey[c + 1] ^= tmp.key[1];				\
    F256_2(tmp.msg, tmp.t);					\
    midmsg[c]     ^= tmp.msg[0];				\
    midmsg[c + 1] ^= tmp.msg[1];

#define ROUND256_OUTPUT(a,b,c,d)				\
    tmp.msg[0] = midmsg[a]	^ midkey[b];			\
    tmp.msg[1] = midmsg[a + 1]	^ midkey[b + 1];		\
    tmp.key[0] = midkey[a]	^ (unsigned int)CNS[d][0];	\
    tmp.key[1] = midkey[a + 1]	^ (unsigned int)CNS[d][1];	\
    F256_2(tmp.key, tmp.t);					\
    midkey[c]     ^= tmp.key[0];				\
    midkey[c + 1] ^= tmp.key[1];				\
    F256_2(tmp.msg, tmp.t);					\
    midmsg[c]     ^= tmp.msg[0];				\
    midmsg[c + 1] ^= tmp.msg[1];

#define ROUND512(a, b, c, d)						\
    tmpkey[0] = midkey[a]	^ (unsigned long long)CNS[d][0];	\
    tmpkey[1] = midkey[a + 1]	^ (unsigned long long)CNS[d][1];	\
    tmpmsg[0] = midmsg[a]	^ midkey[b];				\
    tmpmsg[1] = midmsg[a + 1]	^ midkey[b + 1];			\
    F512_1(tmpkey, t);							\
    midkey[c]     ^= tmpkey[0];						\
    midkey[c + 1] ^= tmpkey[1];						\
    F512_2(tmpmsg, t);							\
    midmsg[c]     ^= tmpmsg[0];						\
    midmsg[c + 1] ^= tmpmsg[1];

#define ROUND512_OUTPUT(a, b, c, d)					\
