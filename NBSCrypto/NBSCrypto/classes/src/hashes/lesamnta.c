static inline int _done256(hash_state *hs, unsigned char *out)
{
    unsigned char *p = (unsigned char*)hs->lesamnta256.buffer;
    int bytePos;
    int bitPos;

    bytePos = hs->lesamnta256.rembitlen >> 3;
    bitPos = (hs->lesamnta256.rembitlen % 8);

    if (bitPos == 0) {
	p[bytePos] = 0x80;
    } else {
	p[bytePos] &= (0xff << (8 - bitPos));
	p[bytePos] |= (0x80 >> bitPos);
    }

    memset(&p[bytePos + 1], 0, LESAMNTA256_BLOCK_LEN - (bytePos + 1));

    if (hs->lesamnta256.rembitlen != 0) {
	BYTES_SWAP256(hs->lesamnta256.buffer);
	_compression256(hs->lesamnta256.hash, hs->lesamnta256.buffer);
	memset(p, 0, LESAMNTA256_BLOCK_LEN);
    }

    BYTES_SWAP256(hs->lesamnta256.buffer);
    hs->lesamnta256.buffer[6] = (unsigned int)(hs->lesamnta256.bitlen[0] >> 32);
    hs->lesamnta256.buffer[7] = (unsigned int)hs->lesamnta256.bitlen[0];
    _output256(hs->lesamnta256.hash, hs->lesamnta256.buffer);
    BYTES_SWAP256(hs->lesamnta256.hash);
    memcpy(out, &hs->lesamnta256.hash, hs->lesamnta256.hashbitlen >> 3);

    return NBSCrypto_OK;
}

