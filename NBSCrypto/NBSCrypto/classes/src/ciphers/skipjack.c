#define A					\
    tmp	= _g_func(w1, &kp, cs->skipjack.k);	\
    w1	= tmp ^ w4 ^ x;				\
    w4	= w3;					\
    w3	= w2;					\
    w2	= tmp;

#define B					\
    tmp = _g_func(w1, &kp, cs->skipjack.k);	\
    tmp1= w4;					\
    w4	= w3;					\
    w3	= w1 ^ w2 ^ x;				\
    w1	= tmp1;					\
    w2	= tmp;

#define A1					\
    tmp	= w1 ^ w2 ^ x;				\
    w1	= _ig_func(w2, &kp, cs->skipjack.k);	\
    w2	= w3;					\
    w3	= w4;					\
    w4	= tmp;

#define B1					\
    tmp	= _ig_func(w2, &kp, cs->skipjack.k);	\
    w2	= tmp ^ w3 ^ x;				\
    w3  = w4;					\
    w4	= w1;					\
    w1	= tmp;


